import React, { useState, useRef, useEffect } from 'react';
import { Storage } from '@plasmohq/storage';
import { API_BASE_URL, type User, type AuthState } from './common/config/supabase';

import "./sidepanel.css"

const storage = new Storage();

// ÂÆö‰πâÊñá‰ª∂Á±ªÂûã
interface FileUpload {
  id: string;
  originalName: string;
  size: number;
  uploadedAt: string;
}

interface Submission {
  id: string;
  fileUpload: FileUpload;
  status: 'UPLOADED' | 'OCR_PROCESSING' | 'AI_PROCESSING' | 'COMPLETED' | 'FAILED';
  submittedAt: string;
  myscriptResults?: Array<{
    id: string;
    recognizedText: string;
    confidence: number;
  }>;
  deepseekResults?: Array<{
    id: string;
    grade: number;
    feedback: string;
    suggestions: string;
    strengths: string;
  }>;
}

const SidePanel: React.FC = () => {
  const [activeTab, setActiveTab] = useState<'upload' | 'results' | 'history'>('results');
  const [status, setStatus] = useState<{ message: string; type: 'success' | 'error' | 'info' }>({ message: '', type: 'info' });
  const [isProcessing, setIsProcessing] = useState(false);
  const [submissions, setSubmissions] = useState<Submission[]>([]);
  const [authState, setAuthState] = useState<AuthState>({
    isAuthenticated: false,
    user: null,
    token: null,
    loading: true
  });
  const [dragOver, setDragOver] = useState(false);
  
  const fileInputRef = useRef<HTMLInputElement>(null);

  useEffect(() => {
    // ÁªÑ‰ª∂Âä†ËΩΩÊó∂ÂàùÂßãÂåñ
    initializeAuth();
  }, []);

  useEffect(() => {
    if (authState.isAuthenticated) {
      loadSubmissions();
    }
  }, [authState.isAuthenticated]);

  // ÂàùÂßãÂåñËÆ§ËØÅÁä∂ÊÄÅ
  const initializeAuth = async () => {
    try {
      const savedToken = await storage.get('auth_token');
      const savedUser = await storage.get('user_info');

      if (savedToken && savedUser) {
        const isValid = await verifyToken(savedToken);
        if (isValid) {
          setAuthState({
            isAuthenticated: true,
            user: savedUser,
            token: savedToken,
            loading: false
          });
          return;
        } else {
          await storage.remove('auth_token');
          await storage.remove('user_info');
        }
      }

      setAuthState(prev => ({ ...prev, loading: false }));
    } catch (error) {
      console.error('ÂàùÂßãÂåñËÆ§ËØÅÁä∂ÊÄÅÂ§±Ë¥•:', error);
      setAuthState(prev => ({ ...prev, loading: false }));
    }
  };

  const verifyToken = async (token: string): Promise<boolean> => {
    try {
      const response = await fetch(`${API_BASE_URL}/auth/verify`, {
        headers: {
          'Authorization': `Bearer ${token}`
        }
      });
      return response.ok;
    } catch (error) {
      console.error('TokenÈ™åËØÅÂ§±Ë¥•:', error);
      return false;
    }
  };

  // Âä†ËΩΩÊèê‰∫§ËÆ∞ÂΩï
  const loadSubmissions = async () => {
    if (!authState.token) return;

    try {
      const response = await fetch(`${API_BASE_URL}/submissions`, {
        headers: {
          'Authorization': `Bearer ${authState.token}`
        }
      });

      if (response.ok) {
        const result = await response.json();
        if (result.success) {
          setSubmissions(result.data.submissions || []);
        }
      }
    } catch (error) {
      console.error('Âä†ËΩΩÊèê‰∫§ËÆ∞ÂΩïÂ§±Ë¥•:', error);
      showStatus('Âä†ËΩΩÊèê‰∫§ËÆ∞ÂΩïÂ§±Ë¥•', 'error');
    }
  };

  // ÊòæÁ§∫Áä∂ÊÄÅ‰ø°ÊÅØ
  const showStatus = (message: string, type: 'success' | 'error' | 'info' = 'info') => {
    setStatus({ message, type });
    
    // 3ÁßíÂêéËá™Âä®ÈöêËóèÔºàÈô§ÈùûÊòØÈîôËØØÔºâ
    if (type !== 'error') {
      setTimeout(() => {
        setStatus({ message: '', type: 'info' });
      }, 3000);
    }
  };

  // Â§ÑÁêÜÊñá‰ª∂ÈÄâÊã©
  const handleFileSelect = (event: React.ChangeEvent<HTMLInputElement>) => {
    const files = event.target.files;
    if (files && files.length > 0) {
      handleFiles(Array.from(files));
    }
  };

  // Â§ÑÁêÜÊãñÊãΩ‰∫ã‰ª∂
  const handleDragOver = (event: React.DragEvent) => {
    event.preventDefault();
    setDragOver(true);
  };

  const handleDragLeave = (event: React.DragEvent) => {
    event.preventDefault();
    setDragOver(false);
  };

  const handleDrop = (event: React.DragEvent) => {
    event.preventDefault();
    setDragOver(false);
    
    const files = event.dataTransfer?.files;
    if (files && files.length > 0) {
      handleFiles(Array.from(files));
    }
  };

  // Â§ÑÁêÜÊñá‰ª∂
  const handleFiles = (files: File[]) => {
    if (!authState.isAuthenticated || !authState.token) {
      showStatus('‚ö†Ô∏è ËØ∑ÂÖàÁôªÂΩïÂêéÂÜç‰∏ä‰º†Êñá‰ª∂', 'error');
      return;
    }

    console.log('Â§ÑÁêÜÊñá‰ª∂:', files);
    showStatus('Ê≠£Âú®‰∏ä‰º†Êñá‰ª∂...', 'info');

    // È™åËØÅÊñá‰ª∂Á±ªÂûã
    const supportedTypes = ['application/pdf', 'image/jpeg', 'image/png', 'image/gif', 'image/webp'];
    const validFiles = files.filter(file => supportedTypes.includes(file.type));

    if (validFiles.length === 0) {
      showStatus('ËØ∑ÈÄâÊã©ÊîØÊåÅÁöÑÊñá‰ª∂Ê†ºÂºè (PDF, JPG, PNG, GIF, WebP)', 'error');
      return;
    }

    // Ê£ÄÊü•Êñá‰ª∂Â§ßÂ∞è
    const maxSize = 100 * 1024 * 1024; // 100MB
    const oversizedFiles = validFiles.filter(file => file.size > maxSize);
    
    if (oversizedFiles.length > 0) {
      showStatus('Êñá‰ª∂Â§ßÂ∞è‰∏çËÉΩË∂ÖËøá100MB', 'error');
      return;
    }

    // ‰∏ä‰º†Êñá‰ª∂
    uploadFiles(validFiles);
  };

  // ‰∏ä‰º†Êñá‰ª∂Âà∞ÂêéÁ´Ø
  const uploadFiles = async (files: File[]) => {
    setIsProcessing(true);
    
    for (const file of files) {
      try {
        showStatus(`Ê≠£Âú®‰∏ä‰º† ${file.name}...`, 'info');

        // ‰∏ä‰º†Êñá‰ª∂
        const formData = new FormData();
        formData.append('file', file);

        const uploadResponse = await fetch(`${API_BASE_URL}/files`, {
          method: 'POST',
          headers: {
            'Authorization': `Bearer ${authState.token}`
          },
          body: formData
        });

        const uploadResult = await uploadResponse.json();

        if (uploadResult.success) {
          // ÂàõÂª∫Êèê‰∫§ËÆ∞ÂΩï
          const submissionResponse = await fetch(`${API_BASE_URL}/submissions`, {
            method: 'POST',
            headers: {
              'Content-Type': 'application/json',
              'Authorization': `Bearer ${authState.token}`
            },
            body: JSON.stringify({
              fileUploadId: uploadResult.data.fileId
            })
          });

          const submissionResult = await submissionResponse.json();
          
          if (submissionResult.success) {
            showStatus(`‚úÖ ${file.name} ‰∏ä‰º†ÊàêÂäüÔºÅ`, 'success');
            
            // ÈáçÊñ∞Âä†ËΩΩÊèê‰∫§ËÆ∞ÂΩï
            await loadSubmissions();
            
            // ÂàáÊç¢Âà∞ÁªìÊûúÊ†áÁ≠æÈ°µ
            setActiveTab('results');
          } else {
            showStatus(`‚ö†Ô∏è ${file.name} ‰∏ä‰º†ÊàêÂäüÔºå‰ΩÜÂàõÂª∫Êèê‰∫§ËÆ∞ÂΩïÂ§±Ë¥•`, 'error');
          }
        } else {
          showStatus(`‚ùå ${file.name} ‰∏ä‰º†Â§±Ë¥•: ${uploadResult.error}`, 'error');
        }
      } catch (error) {
        console.error('‰∏ä‰º†Êñá‰ª∂Â§±Ë¥•:', error);
        showStatus(`‚ùå ${file.name} ‰∏ä‰º†Â§±Ë¥•`, 'error');
      }
    }
    
    setIsProcessing(false);
  };

  // Ëß¶ÂèëOCRÂ§ÑÁêÜ
  const triggerOCR = async (submissionId: string) => {
    if (!authState.token) return;

    try {
      setIsProcessing(true);
      showStatus('Ê≠£Âú®ËøõË°åOCRËØÜÂà´...', 'info');

      const response = await fetch(`${API_BASE_URL}/ocr/myscript`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${authState.token}`
        },
        body: JSON.stringify({
          submissionId: submissionId
        })
      });

      const result = await response.json();

      if (result.success) {
        showStatus('‚úÖ OCRËØÜÂà´ÂÆåÊàêÔºÅ', 'success');
        await loadSubmissions();
      } else {
        showStatus(`‚ùå OCRËØÜÂà´Â§±Ë¥•: ${result.error}`, 'error');
      }
    } catch (error) {
      console.error('OCRÂ§ÑÁêÜÂ§±Ë¥•:', error);
      showStatus('‚ùå OCRÂ§ÑÁêÜÂ§±Ë¥•', 'error');
    } finally {
      setIsProcessing(false);
    }
  };

  // Ëß¶ÂèëAIÊâπÊîπ
  const triggerAIGrading = async (submissionId: string) => {
    if (!authState.token) return;

    try {
      setIsProcessing(true);
      showStatus('Ê≠£Âú®ËøõË°åAIÊâπÊîπ...', 'info');

      const response = await fetch(`${API_BASE_URL}/ai/grade`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${authState.token}`
        },
        body: JSON.stringify({
          submissionId: submissionId
        })
      });

      const result = await response.json();

      if (result.success) {
        showStatus('üéâ AIÊâπÊîπÂÆåÊàêÔºÅ', 'success');
        await loadSubmissions();
      } else {
        showStatus(`‚ùå AIÊâπÊîπÂ§±Ë¥•: ${result.error}`, 'error');
      }
    } catch (error) {
      console.error('AIÊâπÊîπÂ§±Ë¥•:', error);
      showStatus('‚ùå AIÊâπÊîπÂ§±Ë¥•', 'error');
    } finally {
      setIsProcessing(false);
    }
  };

  // Ê∏≤ÊüìÊèê‰∫§È°π
  const renderSubmissionItem = (submission: Submission) => {
    const statusText = {
      'UPLOADED': 'üì§ Â∑≤‰∏ä‰º†',
      'OCR_PROCESSING': 'üîç OCRÂ§ÑÁêÜ‰∏≠',
      'AI_PROCESSING': 'ü§ñ AIÊâπÊîπ‰∏≠',
      'COMPLETED': '‚úÖ Â∑≤ÂÆåÊàê',
      'FAILED': '‚ùå Â§ÑÁêÜÂ§±Ë¥•'
    };

    const hasOCRResults = submission.myscriptResults && submission.myscriptResults.length > 0;
    const hasAIResults = submission.deepseekResults && submission.deepseekResults.length > 0;

    return (
      <div key={submission.id} className="result-item">
        <div className="result-header">
          <div className="result-title">{submission.fileUpload.originalName}</div>
          <div className="result-status">
            {statusText[submission.status]}
          </div>
        </div>

        <div className="result-content">
          <div className="result-time">
            Êèê‰∫§Êó∂Èó¥: {new Date(submission.submittedAt).toLocaleString()}
          </div>

          {hasOCRResults && (
            <div className="ocr-results">
              <h4>üîç OCRËØÜÂà´ÁªìÊûú:</h4>
              {submission.myscriptResults!.map(result => (
                <div key={result.id} className="ocr-result">
                  <p><strong>ËØÜÂà´ÊñáÊú¨:</strong> {result.recognizedText}</p>
                  <p><strong>ÁΩÆ‰ø°Â∫¶:</strong> {(result.confidence * 100).toFixed(1)}%</p>
                </div>
              ))}
            </div>
          )}

          {hasAIResults && (
            <div className="ai-results">
              <h4>ü§ñ AIÊâπÊîπÁªìÊûú:</h4>
              {submission.deepseekResults!.map(result => (
                <div key={result.id} className="ai-result">
                  <div className="grade">
                    <strong>ÂæóÂàÜ:</strong> 
                    <span className={`score ${result.grade >= 80 ? 'good' : result.grade >= 60 ? 'medium' : 'poor'}`}>
                      {result.grade}ÂàÜ
                    </span>
                  </div>
                  <div className="feedback">
                    <strong>ÊâπÊîπÂèçÈ¶à:</strong><br />
                    {result.feedback}
                  </div>
                  {result.suggestions && (
                    <div className="suggestions">
                      <strong>ÊîπËøõÂª∫ËÆÆ:</strong><br />
                      {result.suggestions}
                    </div>
                  )}
                  {result.strengths && (
                    <div className="strengths">
                      <strong>‰ºòÁÇπ:</strong><br />
                      {result.strengths}
                    </div>
                  )}
                </div>
              ))}
            </div>
          )}

          <div className="result-actions">
            {submission.status === 'UPLOADED' && (
              <button 
                className="btn btn-small" 
                onClick={() => triggerOCR(submission.id)}
                disabled={isProcessing}
              >
                ÂºÄÂßãOCRËØÜÂà´
              </button>
            )}
            {hasOCRResults && !hasAIResults && (
              <button 
                className="btn btn-small" 
                onClick={() => triggerAIGrading(submission.id)}
                disabled={isProcessing}
              >
                ÂºÄÂßãAIÊâπÊîπ
              </button>
            )}
          </div>
        </div>
      </div>
    );
  };

  if (authState.loading) {
    return (
      <div className="sidepanel-container">
        <div className="header">
          <h1>üìö AIÂæÆÁßØÂàÜÂä©Êïô</h1>
        </div>
        <div className="loading">
          <div className="spinner"></div>
          <p>Ê≠£Âú®Âä†ËΩΩ...</p>
        </div>
      </div>
    );
  }

  if (!authState.isAuthenticated) {
    return (
      <div className="sidepanel-container">
        <div className="header">
          <h1>üìö AIÂæÆÁßØÂàÜÂä©Êïô</h1>
          <p>Êô∫ËÉΩÊâπÊîπ ¬∑ ÈîôÈ¢òÂàÜÊûê ¬∑ Â≠¶‰π†Âª∫ËÆÆ</p>
        </div>
        <div className="auth-required">
          <div className="auth-icon">üîê</div>
          <h3>ÈúÄË¶ÅÁôªÂΩï</h3>
          <p>ËØ∑Âú®Êèí‰ª∂popup‰∏≠ÁôªÂΩïGitHubË¥¶Êà∑Âêé‰ΩøÁî®‰æßËæπÊ†èÂäüËÉΩ„ÄÇ</p>
        </div>
      </div>
    );
  }

  return (
    <div className="sidepanel-container">
      {/* Â§¥ÈÉ® */}
      <div className="header">
        <h1>üìö AIÂæÆÁßØÂàÜÂä©Êïô</h1>
        <p>Êô∫ËÉΩÊâπÊîπ ¬∑ ÈîôÈ¢òÂàÜÊûê ¬∑ Â≠¶‰π†Âª∫ËÆÆ</p>
        <div className="user-info-small">
          üëã Ê¨¢ËøéÔºå{authState.user?.username}
        </div>
      </div>

      <div className="container">
        {/* Áä∂ÊÄÅÊòæÁ§∫ */}
        {status.message && (
          <div className={`status status-${status.type}`}>
            {status.message}
          </div>
        )}

        {/* Ê†áÁ≠æÈ°µ */}
        <div className="tabs">
          <button 
            className={`tab ${activeTab === 'upload' ? 'active' : ''}`}
            onClick={() => setActiveTab('upload')}
          >
            üì§ ‰∏ä‰º†‰Ωú‰∏ö
          </button>
          <button 
            className={`tab ${activeTab === 'results' ? 'active' : ''}`}
            onClick={() => setActiveTab('results')}
          >
            üìä ÊâπÊîπÁªìÊûú
          </button>
          <button 
            className={`tab ${activeTab === 'history' ? 'active' : ''}`}
            onClick={() => setActiveTab('history')}
          >
            üìã ÂéÜÂè≤ËÆ∞ÂΩï
          </button>
        </div>

        {/* ‰∏ä‰º†‰Ωú‰∏öÊ†áÁ≠æÈ°µ */}
        {activeTab === 'upload' && (
          <div className="tab-content">
            <div className="upload-section">
              <div className="section-title">
                <span>üìù</span>
                ‰∏ä‰º†‰Ωú‰∏öÊñá‰ª∂
              </div>
              
              <div 
                className={`upload-area ${dragOver ? 'dragover' : ''}`}
                onClick={() => fileInputRef.current?.click()}
                onDragOver={handleDragOver}
                onDragLeave={handleDragLeave}
                onDrop={handleDrop}
              >
                <div className="upload-icon">üìÑ</div>
                <div className="upload-text">ÁÇπÂáª‰∏ä‰º†ÊàñÊãñÊãΩÊñá‰ª∂Âà∞ËøôÈáå</div>
                <div className="upload-hint">ÊîØÊåÅ PDF„ÄÅJPG„ÄÅPNG„ÄÅGIF„ÄÅWebP Ê†ºÂºèÔºåÊúÄÂ§ß100MB</div>
              </div>

              <input 
                type="file" 
                ref={fileInputRef}
                className="file-input" 
                accept=".pdf,.jpg,.jpeg,.png,.gif,.webp" 
                multiple
                onChange={handleFileSelect}
              />
              
              <button 
                className="btn"
                onClick={() => fileInputRef.current?.click()}
                disabled={isProcessing}
              >
                {isProcessing ? 'Â§ÑÁêÜ‰∏≠...' : 'ÈÄâÊã©Êñá‰ª∂'}
              </button>
            </div>
          </div>
        )}

        {/* ÊâπÊîπÁªìÊûúÊ†áÁ≠æÈ°µ */}
        {activeTab === 'results' && (
          <div className="tab-content">
            <div className="results-section">
              <div className="section-title">
                <span>üéØ</span>
                ÊúÄÊñ∞ÊâπÊîπÁªìÊûú
                <button 
                  className="btn btn-small refresh-btn" 
                  onClick={loadSubmissions}
                  disabled={isProcessing}
                >
                  üîÑ Âà∑Êñ∞
                </button>
              </div>
              
              <div className="results-container">
                {submissions.length > 0 ? (
                  submissions.map(renderSubmissionItem)
                ) : (
                  <div className="empty-state">
                    <div className="empty-icon">üìù</div>
                    <p>ËøòÊ≤°ÊúâÊèê‰∫§ËÆ∞ÂΩï</p>
                    <p>‰∏ä‰º†‰Ωú‰∏öÂºÄÂßãAIÊâπÊîπÂêßÔºÅ</p>
                  </div>
                )}
              </div>
            </div>
          </div>
        )}

        {/* ÂéÜÂè≤ËÆ∞ÂΩïÊ†áÁ≠æÈ°µ */}
        {activeTab === 'history' && (
          <div className="tab-content">
            <div className="upload-section">
              <div className="section-title">
                <span>üìö</span>
                ÂéÜÂè≤ËÆ∞ÂΩï
              </div>
              
              <div className="results-container">
                {submissions.filter(s => s.status === 'COMPLETED').length > 0 ? (
                  submissions
                    .filter(s => s.status === 'COMPLETED')
                    .map(renderSubmissionItem)
                ) : (
                  <div className="empty-state">
                    <div className="empty-icon">üìä</div>
                    <p>ËøòÊ≤°ÊúâÂÆåÊàêÁöÑÊâπÊîπËÆ∞ÂΩï</p>
                    <p>ÂÆåÊàêÊâπÊîπÂêé‰ºöÂú®ËøôÈáåÊòæÁ§∫</p>
                  </div>
                )}
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

export default SidePanel; 